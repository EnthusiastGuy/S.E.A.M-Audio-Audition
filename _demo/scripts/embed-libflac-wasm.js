'use strict';
/**
 * Writes vendor/libflac-wasm-embed.js so libflac can run from file:// without
 * fetching libflac.min.wasm.wasm (browsers block that for secondary file URLs).
 */
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const wasmPath = path.join(root, 'vendor', 'libflac.min.wasm.wasm');
const outPath = path.join(root, 'vendor', 'libflac-wasm-embed.js');

const buf = fs.readFileSync(wasmPath);
const b64 = buf.toString('base64');

const content =
  `// Auto-generated by scripts/embed-libflac-wasm.js — do not edit.\n` +
  `(function (g) {\n` +
  `  if (!g) return;\n` +
  `  var b64 = ${JSON.stringify(b64)};\n` +
  `  var bin = g.atob(b64);\n` +
  `  var n = bin.length;\n` +
  `  var u8 = new Uint8Array(n);\n` +
  `  for (var i = 0; i < n; i++) u8[i] = bin.charCodeAt(i) & 255;\n` +
  `  g.Module = g.Module || {};\n` +
  `  g.Module.wasmBinary = u8;\n` +
  `})(typeof globalThis !== 'undefined' ? globalThis : typeof window !== 'undefined' ? window : this);\n`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('Wrote', path.relative(root, outPath), `(${buf.length} byte wasm → ${fs.statSync(outPath).size} byte script)`);
