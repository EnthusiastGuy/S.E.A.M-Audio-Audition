/**
 * Reads img/video_background.jpg and writes vendor/seam-video-bg-embed.js
 * so demo video export can decode the background on file:// (fetch is blocked there).
 */
'use strict';

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const imgPath = path.join(root, 'img', 'video_background.jpg');
const outPath = path.join(root, 'vendor', 'seam-video-bg-embed.js');

let dataUrl = '';
if (fs.existsSync(imgPath)) {
  const buf = fs.readFileSync(imgPath);
  dataUrl = `data:image/jpeg;base64,${buf.toString('base64')}`;
  console.log(`Embedded ${imgPath} (${buf.length} bytes) → ${outPath}`);
} else {
  console.warn(`No ${imgPath} — wrote empty SEAM_VIDEO_BG_DATA_URL (add the JPG and re-run).`);
}

const out = `/* eslint-disable -- auto-generated by npm run vendor:video-bg */
'use strict';
globalThis.SEAM_VIDEO_BG_DATA_URL = ${JSON.stringify(dataUrl)};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, out, 'utf8');
